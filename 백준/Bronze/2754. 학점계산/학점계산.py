import sys

C = sys.stdin.readline().rstrip()
if C == "A+":
    print(4.3)
elif C == "A0":
    print(4.0)
elif C == "A-":
    print(3.7)
elif C == "B+":
    print(3.3)
elif C == "B0":
    print(3.0)
elif C == "B-":
    print(2.7)
elif C == "C+":
    print(2.3)
elif C == "C0":
    print(2.0)
elif C == "C-":
    print(1.7)
elif C == "D+":
    print(1.3)
elif C == "D0":
    print(1.0)
elif C == "D-":
    print(0.7)
else:
    print(0.0)